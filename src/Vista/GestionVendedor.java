    //validar campos 
     private boolean validarCampos() {
        String nombre = tnombre.getText().trim();
        String apellido = tapellido.getText().trim();
        String dni = tdni.getText().trim();
        String correo = tcorreo.getText().trim();
        String telef = ttelefono.getText().trim();

        // Verificar que ningún campo esté vacío
        if (nombre.isEmpty() || apellido.isEmpty() || dni.isEmpty() || telef.isEmpty() || correo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor complete todos los campos.");
            return false;
        }

        // Validar que el DNI tenga exactamente 8 dígitos
        if (!dni.matches("\\d{8}")) {
            JOptionPane.showMessageDialog(this, "El DNI debe tener exactamente 8 dígitos numéricos.");
            return false;
        }

        // Validar que el teléfono contenga solo números
        if (!telef.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "El teléfono debe contener solo números.");
            return false;
        }

        // Validar el formato del correo electrónico
        if (!correo.matches("^[\\w.-]+@[\\w.-]+\\.[a-zA-Z]{2,}$")) {
            JOptionPane.showMessageDialog(this, "El correo electrónico no tiene un formato válido.");
            return false;
        }

        // Si todas las validaciones pasan, retornamos true
        return true;
     }
     
        //limpiar los campos de texto
        private void limpiarCampos() {
            tnombre.setText("");
            tapellido.setText("");
            tdni.setText("");
            ttelefono.setText("");
            tcorreo.setText("");
        }

