    private void bregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bregistrarActionPerformed
        if (!validarCampos()) return;

        // Obtener los datos de los campos de texto
        String nombre = tnombre.getText().trim();
        String apellido = tapellido.getText().trim();
        String dni = tdni.getText().trim();
        String correo = tcorreo.getText().trim();
        String telef = ttelefono.getText().trim();

   

        // Insertar el cliente en la base de datos a través del VendedorControlador
        try {
            // Crear una instancia del ClienteControlador con la conexión
            VendedorControlador controlador = new VendedorControlador();
            controlador.insertarVendedor(nombre,apellido,dni,correo,telef);
            // Limpiar campos y mostrar mensaje de éxito
            limpiarCampos();
            JOptionPane.showMessageDialog(this, "Vendedor registrado con éxito.");
        } catch (RuntimeException e){
            JOptionPane.showMessageDialog(this, "Error al registrar vendedor.");
        }
    }//GEN-LAST:event_bregistrarActionPerformed

