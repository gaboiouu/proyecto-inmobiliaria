        //validar campos 
    private boolean validarCampos() {
        String dni = jdni.getText().trim();
        String telefono = jtelefono.getText().trim();
        String correo = jcorreo.getText().trim();

        if (jnombre.getText().trim().isEmpty() || japellido.getText().trim().isEmpty() ||
            jdireccion.getText().trim().isEmpty() || dni.isEmpty() || telefono.isEmpty() ||
            correo.isEmpty() || jgenero.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor complete todos los campos.");
            return false;
        }

        if (!telefono.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "El teléfono debe contener solo números.");
            return false;
        }

        if (!correo.matches("^[\\w.-]+@[\\w.-]+\\.[a-zA-Z]{2,}$")) {
            JOptionPane.showMessageDialog(this, "El correo electrónico no tiene un formato válido.");
            return false;
        }

        return true;
     }
     
