    private void bguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bguardarActionPerformed

        if (!validarCampos()) return;

        // Obtener los datos de los campos de texto
        String nombre = jnombre.getText();
        String apellido = japellido.getText();
        String direccion = jdireccion.getText();
        String correo = jcorreo.getText();
        String dni = jdni.getText();
        String genero = jgenero.getText();
        String telefono = jtelefono.getText();
    
        
        // Validar el DNI
        if (dni.length() != 8 || !dni.matches("\\d{8}")) {
            JOptionPane.showMessageDialog(this, "El DNI debe tener 8 dígitos numéricos.");
            return;
            }

            // Insertar el cliente en la base de datos a través del ClienteControlador
            try {
                ClienteControlador controlador = new ClienteControlador();
                controlador.insertarCliente(nombre, apellido, direccion, correo, dni, genero, telefono);                
                // Limpiar campos y mostrar mensaje de éxito
                limpiarCampos();
                mensaje.registrarCliente();
               // Actualizar el JTable
                cargarDatos("");
            } catch (RuntimeException e) {
                mensaje.errorRegistrar();
            }
    }//GEN-LAST:event_bguardarActionPerformed


